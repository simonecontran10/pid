const SUPPORTED_LANGS = ["it", "en"];
const RTL_LANGS = new Set(["ar"]);

let currentLang = localStorage.getItem("pid_lang") || "it";
if (!SUPPORTED_LANGS.includes(currentLang)) currentLang = "it";

function t(key, ...args) {
  const v = I18N[currentLang]?.[key] ?? I18N.it[key] ?? I18N.en[key] ?? key;
  return typeof v === "function" ? v(...args) : v;
}

function isRTL() {
  return RTL_LANGS.has(currentLang);
}

function applyTranslations() {
  document.documentElement.lang = currentLang;
  document.documentElement.dir = isRTL() ? "rtl" : "ltr";
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const k = el.getAttribute("data-i18n");
    el.textContent = t(k);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const k = el.getAttribute("data-i18n-placeholder");
    el.placeholder = t(k);
  });
  // Stato attivo dei pulsanti lingua nell'header
  document.querySelectorAll(".lang-btn").forEach(b => {
    const isActive = b.id === `lang-${currentLang}`;
    b.classList.toggle("lang-btn-active", isActive);
  });
}

function setLang(lang) {
  if (!SUPPORTED_LANGS.includes(lang)) return;
  currentLang = lang;
  localStorage.setItem("pid_lang", lang);
  applyTranslations();
  document.dispatchEvent(new Event("lang-changed"));
}

document.addEventListener("DOMContentLoaded", () => {
  SUPPORTED_LANGS.forEach(code => {
    const btn = document.getElementById(`lang-${code}`);
    if (btn) btn.addEventListener("click", () => setLang(code));
  });
  applyTranslations();
});
